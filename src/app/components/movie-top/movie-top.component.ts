  constructor(
    private route: ActivatedRoute,
    private _changeDetectorRef: ChangeDetectorRef,
    private renderer2: Renderer2
  ) {}

  ngOnInit(): void {
    this.indice = this.route.snapshot.params['id'];
    let uno = this.movieDetails.vote_average.toString()[0];
    let dos = this.movieDetails.vote_average.toString()[2];
    this.percentage = parseInt(`${uno}${dos}`);
    this.title = `${uno}${dos}`;
    console.log(this.indice);
    this.Stars();
    this.Duration();
    this.getTrailer();
    /////////////////////////////////
    const tag = document.createElement('script');
    tag.src = 'https://www.youtube.com/iframe_api';
    document.body.appendChild(tag);
    /////////////////////////
    let filtrados = this.actores.filter((e) => e.profile_path !== null);
    this.actores = filtrados;

  }

  ngAfterViewInit(): void {
    this.renderer2.listen(this.botontube.nativeElement, 'click', (evt) => {
      this.change();
    });
    this.renderer2.listen(this.covermovie.nativeElement, 'click', (evt) => {
      this.change();
    });
    this.onResize();
    window.addEventListener('resize', this.onResize);
  }

  getURL(post: string) {
    return `${this.URL}${post}`;
  }
  change() {
    if (!this.trailerActive) {
      this.renderer2.addClass(this.youtube.nativeElement, 'videoactive');
      this.renderer2.addClass(this.covermovie.nativeElement, 'coveractive');
      this.trailerActive = true;
    } else {
      this.renderer2.removeClass(this.youtube.nativeElement, 'videoactive');
      this.renderer2.removeClass(this.covermovie.nativeElement, 'coveractive');
      this.trailerActive = false;
    }
  }

  onResize = (): void => {
    // Automatically expand the video to fit the page up to 1200px x 720px
    this.videoWidth = Math.min(
      this.demoYouTubePlayer.nativeElement.clientWidth,
      1200
    );
    this.videoHeight = this.videoWidth * 0.6;
    this._changeDetectorRef.detectChanges();
  };

  ngOnDestroy(): void {
    window.removeEventListener('resize', this.onResize);
  }

  ///////////////////////////////////////////
  Stars() {
    if (this.percentage < 20) {
      this.color2 = '#8DA0BC';
      this.color3 = '#8DA0BC';
      this.color4 = '#8DA0BC';
      this.color5 = '#8DA0BC';
    }
    if (this.percentage > 20 && this.percentage < 40) {
      this.color3 = '#8DA0BC';
      this.color4 = '#8DA0BC';
      this.color5 = '#8DA0BC';
    }
    if (this.percentage > 40 && this.percentage < 70) {
      this.color4 = '#8DA0BC';
      this.color5 = '#8DA0BC';
    }
    if (this.percentage > 70 && this.percentage < 90) {
      this.color5 = '#8DA0BC';
    }
  }
  Duration() {
    this.hours = (this.movieDetails.runtime / 60).toString()[0];
    this.minutes = this.movieDetails.runtime % 60;
  }

  getImage() {
    return `${this.URL}${this.movieDetails.poster_path}`;
  }

  getTrailer() {
    let trailer = this.movieTrailers.find((e) =>
      e.name.toLowerCase().includes('tráiler oficial')
    );
    return `https://www.youtube.com/watch?v=${trailer.key}`;
  }
